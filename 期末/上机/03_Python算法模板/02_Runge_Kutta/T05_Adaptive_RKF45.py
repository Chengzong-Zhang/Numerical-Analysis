"""
教材第五章 §5.3.4：Fehlberg 自适应 Runge-Kutta 4(5) 方法模板。

算法核心思想
------------
1. 每步用同一组 6 个阶段斜率 K1~K6 分别构造「四阶近似 y4」和「五阶近似 y5」。
2. 误差估计：η = y5 - y4 ≈ 四阶公式的局部截断误差（O(h^5)）。
3. 接受准则：无量纲误差 ε = max(|η| / sc) ≤ 1 则接受，否则拒绝并缩小步长重算。
4. 步长更新：h_new = h × safety × ε^(-1/5)，再截断到 [h_min, h_max]。
   （指数 -1/5 来自四阶误差 ∝ h^5，令 h 使误差恰等于容限即得此公式）
5. 接受时推进五阶解 y5（精度更高），四阶解 y4 只参与误差估计。

Fehlberg Butcher 表（教材式 5.3.22 / 5.3.23）
----------------------------------------------
c | B (阶段系数矩阵，下三角)
--+----------------------------------------------
0  |
1/4| 1/4
3/8| 3/32        9/32
12/13| 1932/2197 -7200/2197  7296/2197
1  | 439/216    -8          3680/513   -845/4104
1/2| -8/27       2         -3544/2565  1859/4104  -11/40
--+----------------------------------------------
y4 | 25/216      0          1408/2565  2197/4104   -1/5    0
y5 | 16/135      0          6656/12825 28561/56430 -9/50   2/55

误差系数（y5 - y4 的各 K 权重）：
  K1: 1/360, K2: 0, K3: -128/4275, K4: -2197/75240, K5: 1/50, K6: 2/55
"""  # Python 语法：文件开头的三引号字符串是「模块文档字符串」，可用 help() 或 __doc__ 读取。

# ============================================================
#  导入依赖
# ============================================================
import numpy as np        # Python 语法：import 将外部库引入当前命名空间，as np 是别名；
                          # NumPy 提供 np.abs / np.maximum / np.asarray 等向量运算，
                          # 让同一套代码同时支持标量 ODE 和 ODE 方程组。
import matplotlib.pyplot as plt  # Python 语法：pyplot 是 Matplotlib 的子模块，
                                 # 通过 plt.xxx 调用画图函数；这里画步长历史与数值解对比图。

# ============================================================
#  §1  必须替换的参数：右端函数 + 初值问题设置
# ============================================================

def f(t, y):
    """【必须替换】ODE 右端函数，表示 y' = f(t, y)。

    参数
    ----
    t : float        当前时间节点
    y : float 或 np.ndarray   当前数值近似（标量 ODE 用 float，方程组用一维数组）

    返回
    ----
    float 或 np.ndarray，与 y 形状相同，表示 y'(t) 的近似。

    替换示例
    --------
    标量 ODE：  return -y + t**2 + 1
    方程组：    return np.array([y[1], -y[0]])   # y1'=y2, y2'=-y1
    """
    # ---- 当前填写的测试方程：y' = -y + t^2 + 1，精确解 y = (t+1)^2 - 0.5*exp(-t) ----
    return -y + t**2 + 1.0  # 【必须替换】将此行改为题目中的 f(t, y)。

# ---- 初值问题参数 ----
t0 = 0.0   # 【必须替换】数值分析：求解区间左端点 a，也是初值所在点。
T  = 1.0   # 【必须替换】数值分析：求解区间右端点 b；算法会截短最后一步使恰好到达 T。
y0 = 1.0   # 【必须替换】数值分析：初值 y(t0)；
           #   方程组：写成 np.array([分量1, 分量2, ...], dtype=float)。

# ---- 精确解（可选，用于误差对比；若未知则注释掉下方校验代码）----
def exact(t):
    """【可选替换】精确解 y(t)，用于计算全局误差。未知时删去即可。

    对应方程 y' = -y + t^2 + 1, y(0)=1 的精确解推导：
      积分因子法：e^t y = ∫e^t(t^2+1)dt = e^t(t^2-2t+3) + C
      → y = t^2 - 2t + 3 + C*e^{-t}
      由 y(0)=1：1 = 3 + C → C = -2
      → y(t) = t^2 - 2t + 3 - 2*e^{-t}
    验证：y(1) = 1-2+3-2/e ≈ 1.2642
    """
    return t**2 - 2.0*t + 3.0 - 2.0*np.exp(-t)  # 【可选替换】

# ============================================================
#  §2  步长控制参数（通常只需调整容限）
# ============================================================

h_initial  = 0.1     # 【可替换】数值分析：第一步的候选步长；后续步长完全由误差控制，
                      # 初始值只影响第一步的"起步速度"，通常选 (b-a)/10 即可。

h_min      = 1.0e-8  # 【可替换】数值分析：允许的最小步长。
                      # 若误差超限时步长已经 ≤ h_min，说明问题可能是刚性的，
                      # 此时算法会抛出 RuntimeError，提示换用隐式方法。

h_max      = 0.5     # 【可替换】数值分析：允许的最大步长，防止解变化平缓时步长增长过大，
                      # 从而漏掉可能存在的局部细节（如隐藏的小幅振荡）。

atol       = 1.0e-8  # 【可替换】数值分析：绝对误差容限。
                      # 当解的数值接近 0 时，相对容限趋于无穷严格，
                      # 此时由 atol 给出误差的绝对下界，防止步长无限缩小。

rtol       = 1.0e-6  # 【可替换】数值分析：相对误差容限。
                      # 当解的数值量级较大时，atol 相对来说非常宽松，
                      # 此时由 rtol×|y| 动态收紧误差要求，保证相对精度。

safety     = 0.9     # 【通常不替换】数值分析：安全系数（< 1）。
                      # 理论最优步长 h* = h × ε^(-1/5)；
                      # 乘以 0.9 是让建议值保守一些，减少下一步被拒绝的概率。

factor_min = 0.2     # 【通常不替换】数值分析：单次调整的步长最小缩放倍数。
                      # 防止误差极大时步长一下子缩到 0，导致步长序列产生突变。

factor_max = 5.0     # 【通常不替换】数值分析：单次调整的步长最大放大倍数。
                      # 防止某一步误差偶然极小（如接近机器精度）时步长激增，
                      # 引发下一步大概率被拒绝的"反弹"。

# ============================================================
#  §3  初始化存储容器
# ============================================================

t_values     = [float(t0)]              # Python 语法：方括号 [] 创建列表，float() 强制浮点；
                                         # 数值分析：自适应步长导致节点总数未知，故用可动态扩展的列表。

y_values     = [np.asarray(y0, dtype=float)]  # Python 语法：np.asarray 把任意输入统一转成 NumPy 浮点数组；
                                               # 数值分析：统一表示使得同一代码支持标量与方程组。

accepted_h   = []  # Python 语法：[] 创建空列表，后续用 list.append() 追加；
                   # 数值分析：保存每个被接受步骤实际使用的步长，用于可视化步长变化。

error_history = []  # Python 语法：同上；数值分析：保存每个接受步的无量纲误差 ε，
                    # 正确实现时所有值应在 (0, 1]，等于 1 时恰好在容限边界。

h             = float(h_initial)  # Python 语法：创建当前候选步长，每次循环后根据误差更新。
rejected_steps = 0                 # Python 语法：整数计数器；数值分析：拒绝次数过多
                                   # 通常说明问题是刚性的，或容限太严，或初始步长过大。

# ============================================================
#  §4  主循环：逐步推进时间
# ============================================================
# 数值分析：外层 while 控制"时间推进"；每次迭代尝试一步，
#   接受则时间前进，拒绝则时间停在原处（缩小 h 后重试）。

while t_values[-1] < T:  # Python 语法：[-1] 取列表最后一个元素，即当前已接受的最新时间节点。
                          # 数值分析：直到时间到达右端点 T 为止（最后一步可能因截断而精确到达 T）。

    # ---- 读取当前已接受的节点与解 ----
    tn = t_values[-1]   # Python 语法：赋值读取列表末元素；数值分析：tn 是本次尝试的起始时间。
    yn = y_values[-1]   # Python 语法：同上；数值分析：yn 是 tn 处已接受的数值近似。

    # ---- 确定本次候选步长（三路截断）----
    # 数值分析：
    #   min(h, h_max) ：不超过用户设定的最大步长；
    #   min(..., T-tn)：最后一步截短以恰好落在 T，而不是越过 T。
    hn = min(h, h_max, T - tn)  # Python 语法：内置 min 接受任意多个参数，返回最小值。

    # ============================================================
    #  §4.1  计算 6 个 Fehlberg 阶段斜率 K1 ~ K6
    # ============================================================
    # 数值分析：每个 Ki = f(tn + ci*hn, yn + hn * Σ b_{ij} Kj)，
    #   系数来自 Butcher 表；显式方法的特点是 Ki 只用前面已算出的斜率。

    # K1：在当前节点 (tn, yn) 处取斜率（c1=0，不前移时间）
    k1 = f(tn, yn)
    # Python 语法：直接调用上方定义的函数 f；
    # 数值分析：K1 被 4 阶和 5 阶公式共用，也是此步骤唯一"真正代价"之一。

    # K2：前移 h/4（c2=1/4），用 K1 做 1/4 步欧拉预测
    k2 = f(tn + hn / 4.0,
           yn + hn * k1 / 4.0)
    # Python 语法：运算符优先级与数学相同，/ 和 * 高于 +；
    # 数值分析：b21 = 1/4，与 c2 = 1/4 一致（一致性要求 Σ b_{rj} = c_r）。

    # K3：前移 3h/8（c3=3/8），用 K1 和 K2 加权
    k3 = f(tn + 3.0 * hn / 8.0,
           yn + hn * (3.0 * k1 + 9.0 * k2) / 32.0)
    # 数值分析：b31=3/32, b32=9/32；注意 3/32 + 9/32 = 12/32 = 3/8 = c3，满足一致性。

    # K4：前移 12h/13（c4=12/13），用 K1、K2、K3 加权
    k4 = f(tn + 12.0 * hn / 13.0,
           yn + hn * (1932.0 * k1 - 7200.0 * k2 + 7296.0 * k3) / 2197.0)
    # 数值分析：分母 2197 = 13^3；b41=1932/2197, b42=-7200/2197, b43=7296/2197；
    # 注意 K2 系数为负，说明该方向修正是反向的（数值上合理，由 Fehlberg 优化得到）。

    # K5：前移 h（c5=1，即步终点），用 K1~K4 加权
    k5 = f(tn + hn,
           yn + hn * (439.0 * k1 / 216.0
                      - 8.0 * k2
                      + 3680.0 * k3 / 513.0
                      - 845.0 * k4 / 4104.0))
    # Python 语法：长表达式分多行写，只需保证括号未闭合即可续行（隐式续行）；
    # 数值分析：K5 在步终点 tn+hn 处评估；b51=439/216≈2.03, b52=-8, b53≈7.17, b54≈-0.21。

    # K6：前移 h/2（c6=1/2，注意不是步终点！），用 K1~K5 加权
    k6 = f(tn + hn / 2.0,
           yn + hn * (-8.0 * k1 / 27.0
                      + 2.0 * k2
                      - 3544.0 * k3 / 2565.0
                      + 1859.0 * k4 / 4104.0
                      - 11.0 * k5 / 40.0))
    # 数值分析：K6 只在 5 阶公式中出现（4 阶公式系数 c6=0），
    #   它使 5 阶公式精度更高，同时也用于估计 4 阶公式的误差。
    #   b61=-8/27≈-0.30, b62=2, b63≈-1.38, b64≈0.45, b65=-0.275。

    # ============================================================
    #  §4.2  构造 4 阶近似 y4 和 5 阶近似 y5
    # ============================================================
    # 数值分析：
    #   y4 是教材式 (5.3.23)，四阶精度，局部误差 O(h^5)；
    #   y5 是教材式 (5.3.22)，五阶精度，局部误差 O(h^6)；
    #   两者共享 K1~K6，额外计算量为 0。

    # 四阶近似：权重向量 c^(4) = (25/216, 0, 1408/2565, 2197/4104, -1/5, 0)
    # 注意 K2 和 K6 的系数均为 0，说明它们不直接进入 4 阶结果
    y4 = yn + hn * (25.0 * k1 / 216.0
                    + 1408.0 * k3 / 2565.0
                    + 2197.0 * k4 / 4104.0
                    - k5 / 5.0)
    # 数值分析：c1^(4)=25/216≈0.116, c3^(4)=1408/2565≈0.549,
    #           c4^(4)=2197/4104≈0.535, c5^(4)=-1/5=-0.2。

    # 五阶近似：权重向量 c^(5) = (16/135, 0, 6656/12825, 28561/56430, -9/50, 2/55)
    y5 = yn + hn * (16.0 * k1 / 135.0
                    + 6656.0 * k3 / 12825.0
                    + 28561.0 * k4 / 56430.0
                    - 9.0 * k5 / 50.0
                    + 2.0 * k6 / 55.0)
    # 数值分析：c1^(5)=16/135≈0.119, c3^(5)≈0.519, c4^(5)≈0.506,
    #           c5^(5)=-9/50=-0.18, c6^(5)=2/55≈0.036；K2 系数仍为 0。

    # ============================================================
    #  §4.3  局部误差估计与无量纲化
    # ============================================================
    # 数值分析：
    #   η = y5 - y4 ≈ 四阶公式的局部截断误差（O(h^5)）。
    #   注意：这里误差估计是后验的（aposteriori），即利用计算结果本身估计误差，
    #   而不需要事先知道精确解。
    #
    #   无量纲误差尺度 sc 采用「混合容限」：
    #     sc_i = atol + rtol × max(|yn_i|, |y5_i|)
    #   - atol 主导：当解接近 0（|y|≈0）时，避免容限趋于 0；
    #   - rtol 主导：当解量级较大（|y|≫1）时，保证相对精度；
    #   - 用 max(|yn|, |y5|) 而非 |yn|：防止接受/拒绝边界反复横跳。

    # 计算混合容限尺度（逐分量，支持方程组）
    scale = atol + rtol * np.maximum(np.abs(yn), np.abs(y5))
    # Python 语法：np.maximum(a, b) 是逐元素取大值（不同于 max(a,b) 对数组无效）；
    #              np.abs 对数组和标量均逐元素取绝对值。
    # 数值分析：scale 的单位与 y 相同，用于归一化误差。

    # 无量纲误差：取方程组各分量中最坏的那个（最大值范数）
    error_ratio = float(np.max(np.abs(y5 - y4) / scale))
    # Python 语法：np.max 在整个数组中取最大值；float() 把 0 维 NumPy 数组转成 Python 标量；
    #              np.abs(y5 - y4) / scale 是逐元素除法。
    # 数值分析：error_ratio = ε = max_i |η_i / sc_i|；
    #   ε ≤ 1 表示所有分量的误差均满足容限；ε > 1 则至少一个分量超限。

    # ============================================================
    #  §4.4  接受或拒绝当前步骤
    # ============================================================
    if error_ratio <= 1.0:
        # Python 语法：if 条件为真时执行缩进块；
        # 数值分析：无量纲误差 ≤ 1 意味着 |η| ≤ sc = atol + rtol*|y|，即误差满足用户要求。

        # 推进时间节点（只有接受步才前进！）
        t_values.append(tn + hn)
        # Python 语法：list.append(x) 在列表末尾追加 x；
        # 数值分析：新节点 tn + hn 与前面各节点共同构成非均匀时间网格。

        # 推进解：用五阶近似（精度更高）
        y_values.append(y5)
        # 数值分析：虽然误差估计用的是「y4 的误差」，但推进解时用精度更高的 y5，
        #   这是 RKF45 相比 RK4(4) 的优势：同样计算 6 个阶段，推进精度更高。

        accepted_h.append(hn)      # 记录被接受的实际步长，用于绘图分析。
        error_history.append(error_ratio)  # 记录归一化误差（应在 (0, 1]）。

    else:
        # Python 语法：else 对应 error_ratio > 1 的情况；
        # 数值分析：拒绝本步 → 时间不推进，仍在 tn 处重新尝试（用更小的 h）。
        rejected_steps += 1
        # Python 语法：+= 1 是自增语法糖，等价于 rejected_steps = rejected_steps + 1；
        # 数值分析：统计拒绝次数，反映步长控制效率。

    # ============================================================
    #  §4.5  计算下一候选步长
    # ============================================================
    # 数值分析：
    #   局部误差 ≈ C·h^5，令 C·h_new^5 = TOL·sc，解得 h_new = h·ε^(-1/5)。
    #   乘以安全系数 safety = 0.9，使建议值保守 10%，减少拒绝概率。
    #
    #   推导过程（考试常考）：
    #     误差 η ≈ C·hn^5，希望 η_new ≈ TOL·sc，则：
    #       C·h_new^5 = TOL·sc
    #       (h_new/hn)^5 = TOL·sc / |η| = 1/ε
    #       h_new = hn · ε^(-1/5)
    #     加安全系数：h_new = hn · safety · ε^(-1/5)

    if error_ratio == 0.0:
        # Python 语法：== 判断是否相等；
        # 数值分析：误差恰好为 0（机器精度下的精确步），直接用最大放大倍数。
        factor = factor_max
    else:
        factor = safety * error_ratio ** (-1.0 / 5.0)
        # Python 语法：** 是幂运算，-1.0/5.0 = -0.2；
        # 数值分析：error_ratio^(-0.2) 是调整倍数的理论值；safety=0.9 是安全系数。

    # 限制调整倍数在 [factor_min, factor_max] 内
    factor = min(factor_max, max(factor_min, factor))
    # Python 语法：嵌套 min/max，等价于 clamp(factor, factor_min, factor_max)；
    # 数值分析：
    #   下界 factor_min=0.2：防止步长突然缩小到原来 1/5 以下（过度收缩）；
    #   上界 factor_max=5.0：防止步长突然放大 5 倍以上（误差偶然为 0 时的保护）。

    # 更新候选步长并限制在 [h_min, h_max]
    h = min(h_max, max(h_min, hn * factor))
    # 数值分析：
    #   h_min：若误差还是超限但步长已达下限，说明方法无法继续（见下方错误检测）；
    #   h_max：即使建议值很大，也不超过用户设定的上限。

    # ============================================================
    #  §4.6  失败检测：步长已到下限仍不满足误差要求
    # ============================================================
    if error_ratio > 1.0 and h <= h_min:
        # Python 语法：and 要求两侧条件同时成立；
        # 数值分析：这意味着即使取最小步长，误差也不可能降到容限以内。
        #   常见原因：
        #   ① 问题是刚性的（Stiff），应换用隐式方法（BDF / Gauss IRK）；
        #   ② atol/rtol 设置过于严格，超出方法能达到的精度范围；
        #   ③ 初始步长过大导致积累误差传播。
        raise RuntimeError(
            f"步长已降至 h_min={h_min:.2e}，局部误差仍为容限的 {error_ratio:.2f} 倍。\n"
            "建议：① 减小 h_min；② 放宽 atol/rtol；"
            "③ 改用适合刚性问题的隐式方法（如 BDF）。"
        )
        # Python 语法：raise 语句主动抛出异常，终止程序并显示错误信息；
